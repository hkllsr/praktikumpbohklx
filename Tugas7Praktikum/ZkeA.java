public class ZkeA {

    public static void main(String[] args) {
        System.out.println("Huruf Z - A:");
        hurufZtoA();
    }

    public static void hurufZtoA() {
        for (char huruf = 'Z'; huruf >= 'A'; huruf--) {
            System.out.print(huruf + " ");
        }
        System.out.println();
    }
}
